#include "pipex.h"

char	*str(char	*s)
{
	int		l;
	int		i;
	int		j;
	char	*p;

	l = 0;
	i = 0;
	j = 0;
	while(s[i] && s[i] == ' ')
	{
		i++;
		j++;
	}
	while(s[i] && s[i] != ' ')
	{
		i++;
		l++;
	}
	p = malloc(l + 1);
	if (!p)
		ft_error();
	i = -1;
	while (++i < l)
		p[i] = s[i + j];
	p[i] = '\0';
	return (p);
}
